/*--------------------------------
Problem 13: Area and Perimeter of Square

Write a program that takes the side of a square as input and calculates both its area and perimeter, then
displays both results.
--------------------------------*/

const a = 25;

const areaOfSquare = a ** 2;
const perimeterOfSquare = 4 * a;

console.log(`Area of Square is: ${areaOfSquare}`);
console.log(`Perimeter of Square is: ${perimeterOfSquare}`);

/* OUTPUT:
Area of Square is: 625
Perimeter of Square is: 100
*/
