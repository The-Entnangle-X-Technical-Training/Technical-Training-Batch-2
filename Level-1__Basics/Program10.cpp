#include <iostream>
using namespace std;

int main() 
{
    double length, width, area ;
    cout << " Enter the length of the rectangle : " ;
    cin >> length ;
    cout << " Enter the width of the rectangle : " ;
    cin >> width ;
    area = length * width ;
    cout << " Area of the given rectangle = " << area << endl ;
    return 0;
}
