#Problem 18: Average of Marks in 5 subjects
First_subject = float(input("enter the marks of first subject: "))
Second_subject = float(input("enter the marks of second subject: "))
third_subject = float(input("enter the marks of third subject: "))
Fourth_subject = float(input("enter the marks of fourth subject: "))
Fifth_subject = float(input("enter the marks of fifth subject: "))

Total = First_subject + Second_subject + third_subject + Fourth_subject + Fifth_subject
Average = (Total) / 5

print("Total marks in 5 subjects is ", Total)
print("The average of five subjects is", Average)
